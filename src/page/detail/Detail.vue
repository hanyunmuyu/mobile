                <div class="show-content-free">
                    <p>在自媒体写作时代，每个人都应该必备一个个人IP品牌标志！</p><p>你可能不知道有一个属于自己的品牌logo，是很有必要的一件事。你的IP品牌logo就代表着你，当读者一看到这个logo就会知道这是你的品牌，通过不停地增加印象，让你的读者记忆深刻。</p><p>今天分享一个简单的logo设计，标志设计本是矢量软件CDR、AI制作的，为了方便学习，这里暂时运用了PS设计，后期会有正式的标志设计教程。先看一张效果图：</p><div class="image-package">
                    <div class="image-container" style="max-width: 695px; max-height: 541px; background-color: transparent;">
                        <div class="image-container-fill" style="padding-bottom: 77.84%;"></div>
                        <div class="image-view" data-width="695" data-height="541"><img data-original-src="//upload-images.jianshu.io/upload_images/5901395-5ce87a5ec28c5f39.png" data-original-width="695" data-original-height="541" data-original-format="image/png" data-original-filesize="135378" class="" src="//upload-images.jianshu.io/upload_images/5901395-5ce87a5ec28c5f39.png?imageMogr2/auto-orient/strip%7CimageView2/2/w/695"></div>
                    </div>
                    <div class="image-caption"></div>
                </div><div class="image-package  ">
                    <div class="image-container" style="max-width: 506px; max-height: 344px; background-color: transparent;">
                        <div class="image-container-fill" style="padding-bottom: 67.97999999999999%;"></div>
                        <div class="image-view" data-width="506" data-height="344"><img data-original-src="//upload-images.jianshu.io/upload_images/5901395-251d9addf4a6491a.jpg" data-original-width="506" data-original-height="344" data-original-format="" data-original-filesize="64471" class="" src="//upload-images.jianshu.io/upload_images/5901395-251d9addf4a6491a.jpg?imageMogr2/auto-orient/strip%7CimageView2/2/w/506"></div>
                    </div>
                    <div class="image-caption"></div>
                </div><p>下面开始：</p><p>1、执行【文件】 —【 新建】，新建一个画布，画布尺寸定为宽500像素，高为500像素，分辨率为72，或快捷键【Ctrl】+【N】，点击确定，如图：</p><div class="image-package">
                    <div class="image-container" style="max-width: 700px; max-height: 379px; background-color: transparent;">
                        <div class="image-container-fill" style="padding-bottom: 54.169999999999995%;"></div>
                        <div class="image-view" data-width="1920" data-height="1040"><img data-original-src="//upload-images.jianshu.io/upload_images/5901395-927a4b1599babb86.png" data-original-width="1920" data-original-height="1040" data-original-format="image/png" data-original-filesize="194888" class="" src="//upload-images.jianshu.io/upload_images/5901395-927a4b1599babb86.png?imageMogr2/auto-orient/strip%7CimageView2/2/w/700"></div>
                    </div>
                    <div class="image-caption"></div>
                </div><p>2、执行【椭圆工具】，按shift键，绘制一个正圆，填充红色，如图：</p><div class="image-package">
                    <div class="image-container" style="max-width: 700px; max-height: 383px; background-color: transparent;">
                        <div class="image-container-fill" style="padding-bottom: 54.76%;"></div>
                        <div class="image-view" data-width="1903" data-height="1042"><img data-original-src="//upload-images.jianshu.io/upload_images/5901395-b52dbaf95655d341.png" data-original-width="1903" data-original-height="1042" data-original-format="image/png" data-original-filesize="193630" class="" src="//upload-images.jianshu.io/upload_images/5901395-b52dbaf95655d341.png?imageMogr2/auto-orient/strip%7CimageView2/2/w/700"></div>
                    </div>
                    <div class="image-caption"></div>
                </div><p>3、执行【横排文字工具】，分别输入“一河漪沫”示例文字，结束输入按小键盘的回车键，选择好字体和大小，调整到合适的位置，填充白色即可，如图：</p><div class="image-package">
                    <div class="image-container" style="max-width: 700px; max-height: 380px;">
                        <div class="image-container-fill" style="padding-bottom: 54.32%;"></div>
                        <div class="image-view" data-width="1911" data-height="1038"><img data-original-src="//upload-images.jianshu.io/upload_images/5901395-05e5b46f6b494991.png" data-original-width="1911" data-original-height="1038" data-original-format="image/png" data-original-filesize="208747" class="image-loading"></div>
                    </div>
                    <div class="image-caption"></div>
                </div><p>4、将文字图层右击【删格化文字】，分别对“一河漪沫”示例文字进行编辑，如图：</p><div class="image-package">
                    <div class="image-container" style="max-width: 700px; max-height: 393px;">
                        <div class="image-container-fill" style="padding-bottom: 56.25%;"></div>
                        <div class="image-view" data-width="1920" data-height="1080"><img data-original-src="//upload-images.jianshu.io/upload_images/5901395-f3f1b5259a18d2f7.png" data-original-width="1920" data-original-height="1080" data-original-format="image/png" data-original-filesize="269975" class="image-loading"></div>
                    </div>
                    <div class="image-caption"></div>
                </div><p>5、执行【钢笔工具】或【矩形选择工具】辅助选择，将“一河漪沫”文字中多余的笔划选中删除，用其他的元素进行替代，先将“一”和“河进行编辑”，得到以下效果，如图：</p><div class="image-package">
                    <div class="image-container" style="max-width: 700px; max-height: 375px;">
                        <div class="image-container-fill" style="padding-bottom: 53.71%;"></div>
                        <div class="image-view" data-width="1912" data-height="1027"><img data-original-src="//upload-images.jianshu.io/upload_images/5901395-d0fcdefe124df9e9.png" data-original-width="1912" data-original-height="1027" data-original-format="image/png" data-original-filesize="200991" class="image-loading"></div>
                    </div>
                    <div class="image-caption"></div>
                </div><p>6、再将“漪”和“沫”进行编辑，可以用矩形选择工具辅助选择，得到以下效果，如图：</p><div class="image-package">
                    <div class="image-container" style="max-width: 700px; max-height: 379px;">
                        <div class="image-container-fill" style="padding-bottom: 54.26%;"></div>
                        <div class="image-view" data-width="1911" data-height="1037"><img data-original-src="//upload-images.jianshu.io/upload_images/5901395-56b561cb058537d8.png" data-original-width="1911" data-original-height="1037" data-original-format="image/png" data-original-filesize="215912" class="image-loading"></div>
                    </div>
                    <div class="image-caption"></div>
                </div><p>7、执行【钢笔工具】，将“漪”和“河”的笔画进行编辑，得到以下效果，如图：</p><div class="image-package">
                    <div class="image-container" style="max-width: 700px; max-height: 380px;">
                        <div class="image-container-fill" style="padding-bottom: 54.31%;"></div>
                        <div class="image-view" data-width="1913" data-height="1039"><img data-original-src="//upload-images.jianshu.io/upload_images/5901395-4542609eced961cf.png" data-original-width="1913" data-original-height="1039" data-original-format="image/png" data-original-filesize="221883" class="image-loading"></div>
                    </div>
                    <div class="image-caption"></div>
                </div><p>8、执行【保存】PSD源文件，图稿文件名，方便以后更改图片和文字，再执行【另存为】，另存一个JPG的图就可以使用了。</p><div class="image-package">
                    <div class="image-container" style="max-width: 700px; max-height: 384px;">
                        <div class="image-container-fill" style="padding-bottom: 54.99000000000001%;"></div>
                        <div class="image-view" data-width="1915" data-height="1053"><img data-original-src="//upload-images.jianshu.io/upload_images/5901395-6973581e1de4999f.png" data-original-width="1915" data-original-height="1053" data-original-format="image/png" data-original-filesize="292018" class="image-loading"></div>
                    </div>
                    <div class="image-caption"></div>
