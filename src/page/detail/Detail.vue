                        <p><b>01</b></p>
                        <p>奶茶刘若英导演的处女作电影《后来的我们》已经发布了预告片，短短两分钟半，看哭了无数有故事的人。</p>
                        <p>电影讲述了一对北漂情侣的故事，方小晓和林见清相识于一列返乡的火车上。</p>
                        <p>方小晓丢了火车票，被林见清捡到了，两人的缘分由此开始。</p>
                        <p>那时候，他们还很朴素青涩，扛着大包小包，满怀着对未来的憧憬，来到北京打拼。</p>
                        <p>路上，方小晓从出租车里伸出头来大喊：“北京，你听到了吗？我们很快就发了！”</p>
                        <p>但他们并没有很快就发了，而是租住在破旧狭窄的出租屋隔间里，白天被领导骂的狗血淋头，晚上穷得吃泡面。</p>
                        <p>生活慢慢露出它残酷的一面，身处其中的人，不得不妥协作出改变。</p>
                        <p>每一对情侣，刚开始的时候都是甜蜜的。但现实太残酷，每个人都苦不堪言，转而对对方有了各种要求。</p>
                        <p>但当林见清终于变成方小晓想要的样子时，方小晓却已经不是以前的那个方小晓了。</p>
                        <p>从相爱，到互相敌对，再到彼此辜负，最后分离。</p>
                        <p>说不清是谁的错，但结果就是双方都伤痕累累。</p>
                        <p><b>明明很相爱，明明都在为对方而努力，但为什么最后就是不能好好在一起？</b></p>
                        <p></p>
                        <p>也许，成长就是明白了生活有时候真的很无奈。</p>
                        <p>等到他们在各自的世界里想清楚，该放下的放下，该坚持的坚持，如果那时候还能在一起，那就好好珍惜。</p>
                        <p></p>
                        <p>年少的时候，我们总是敏感多疑，爱跟对方较劲，对什么都较真。</p>
                        <p>而长大后你会知道，生活才是最大的敌人，<b>对待爱情，有时候要钝一点才会幸福</b>。</p>
