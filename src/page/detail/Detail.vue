                        <p><b>02</b></p>
                        <p>不得不说，刘若英对爱情的理解是深刻的，电影中，她对爱情的刻画，很打动人心。</p>
                        <p>《后来的我们》是刘若英导演的处女作。</p>
                        <p>有人说，第一部作品里，一定有自己的影子。</p>
                        <p>奶茶刘若英在这部电影里，又放进去自己人生的哪些影子呢？</p>
                        <p>提起奶茶，就绕不开<b>陈升</b>这个名字，他是她的恩师，也是她暗生情愫的人。</p>
                        <p>但可惜，君生我未生，刘若英出现时，陈升已为人夫。</p>
                        <p>那时的奶茶，还能勇敢地唱着《为爱痴狂》，她是真的想过，一直等着他啊。</p>
                        <p>2002年，陈升在台北举办演唱会。</p>
                        <p>演出结束后，刘若英走过来，站在陈升面前，当众问道：“你能给我一个拥抱吗？”</p>
                        <p>在无数歌迷面前，陈升最后也只是用手掌轻轻拍了拍刘若英的头，正如一个师父对待徒弟那样。</p>
                        <p>陈升能给刘若英的，只有事业上的倾力相助，然后看着她一路事业飞升，大放异彩。</p>
                        <p>在综艺节目《桃色蛋白质》中，主持人问道：“你喜欢刘若英吗？”</p>
                        <p>陈升坦白地说：“我当然喜欢她，否则我为什么为她做这么多事情。”</p>
                        <p>而后，陈升又说：“现在她像风筝，不知已经飘到什么地方？”</p>
                        <p>刘若英哭得像个孩子：“<b>你可以拉拉线啊，风筝的线永远在你手里。</b>”</p>
                        <p>陈升沉默后说道：“可是我找不到线了。”</p>
                        <p>后来的奶茶，结婚生子，过着自己想要的生活。只是在演唱会上唱起《后来》这首歌，她还是情不自禁流泪大哭。</p>
                        <p>后来</p>
                        <p>我总算学会了如何去爱</p>
                        <p>可惜你早已远去</p>
                        <p>消失在人海</p>
                        <p>后来终于在眼泪中明白</p>
                        <p>有些人一旦错过就不再</p>
                        <p>这首歌太像她自己的心境。</p>
                        <p>后来，我们没有在一起，我也过得很好，但我还是会忍不住想起你，想像着你如何回忆我，是笑着，还是只有沉默。</p>
                        <p><b>03</b></p>
                        <p>微博上有个话题：<b>知道深爱的人结婚了，你那一刻是什么感觉？</b></p>
                        <p>有人说，其实挺开心的，你这么优秀如果还不结婚就浪费了，真感谢你婚礼从不打算请我，不然我都不知道该借哪个帅哥去参加。</p>
                        <p>话说着，已泪流满面。</p>
                        <p>有人说，深秋的街头，她抱着孩子从我们当年常去的一家饭馆里出来，我愣在那里，她抬起头，四目相对。</p>
                        <p>我感觉像是自己错过了另一种人生。</p>
                        <p>有人说，最难过的，莫过于当你遇上一个特别的人，却明白永远不可能在一起，或迟或早，你不得不放弃。</p>
                        <p><b>真心喜欢过的人是没法做朋友的，哪怕再多看几眼，都还是想拥有。我从未放弃过爱你，只是从浓烈变得悄无声息。</b></p>
                        <p>还有人说，那天我早上六点起床，想着他应该去接新娘了，我默默吃了早饭，喝了点酒，强迫自己睡去。</p>
                        <p>晚上七点醒来，估计着他应该和亲戚喝酒要洞房了，我关了手机在陌生的大城市转悠了一个晚上，等到12点，心想终于熬过了这难熬的一天，然后蹲在马路上哭了。</p>
                        <p>几万条评论，几万个伤心的人。</p>
                        <p>佛曰，人有八苦，生、老、病、死、怨憎会、爱别离、求不得、五阴盛。</p>
                        <p><b>求而不得的爱，是谁都无法避免的苦。</b></p>
                        <p>所以，每个人都可以从《后来》中听出自己的故事，每个人也都可以从《后来》中品出自己的伤痛。</p>
