                        <p><b>04</b></p>
                        <p><b>有一则玄妙的佛教故事。<br></b></p>
                        <p>从前，有一座圆音寺，香火旺盛，受禅语熏陶，寺庙前横梁上的蜘蛛也有了佛性。</p>
                        <p>忽然有一天，佛祖光临圆音寺，见蜘蛛有灵性，便问它：“世间什么才是最珍贵的？”</p>
                        <p>蜘蛛答道：“我觉得最珍贵的是‘得不到’和‘已失去’。”</p>
                        <p>过了一千年，风吹来一滴甘露，蜘蛛见它晶莹剔透，顿生爱意，每日看着，异常开心。</p>
                        <p>但又刮了一阵风，把甘露吹走了，蜘蛛感到寂寞更甚从前。</p>
                        <p>这时，佛祖又来了，问蜘蛛：“世间什么才是最珍贵的？”</p>
                        <p>蜘蛛想着甘露，仍回答：“世间最珍贵的是‘得不到’和‘已失去’。”</p>
                        <p>佛祖便邀它去人间走一趟。</p>
                        <p>于是，蜘蛛投胎到一个官宦人家，成为一名富家小姐，名叫蛛儿。</p>
                        <p>蛛儿16岁时，见到新科状元甘鹿，为他倾倒。</p>
                        <p>她一心以为，这是佛祖为她安排的姻缘。</p>
                        <p>但最后，皇上下诏，赐婚新科状元甘鹿和长风公主，并命太子芝草和蛛儿完婚。</p>
                        <p>蛛儿一气之下绝食，性命垂危时，佛祖来了，告诉她：</p>
                        <p>甘露，本来就是风带来的，所以甘鹿是属于长风公主的，而太子芝草，是当年圆音寺门前的一颗小草，他爱慕了你三千年，你却从未看过他。</p>
                        <p>此时，蛛儿才顿悟，<b>世间最珍贵的不是“得不到”和”“已失去”，而是现在能把握的幸福。</b></p>
                        <p><b>很多人想要把前任追回来，并不是出于深爱，而是出于对“未完成”的感情的不甘心。</b></p>
                        <p>但你们之所以分手，一定是有无法解决的矛盾在其中。</p>
                        <p>强求复合的结果，很可能只是重蹈覆辙。</p>
                        <p></p>
                        <p><b>得不到的和已失去的，都不值得你恋恋不舍。有些情愫，放在心底，某年某月想起来，能会心一笑，就够了。</b></p>
                        <p><b>05</b></p>
                        <p><b>对待爱情的正确态度，应该是怎样的呢？<br></b></p>
                        <p>电视剧《老男孩》中，女主角林小欧曾经深爱着男朋友叶昕。</p>
                        <p>但当得知叶昕出轨后，林小欧特意飞到澳洲，费劲周折，见到叶昕，当面和他做了了结。</p>
                        <p>经过一段时间的沉湎之后，林小欧还是振作起来了，尽职尽责做一名好老师。</p>
                        <p>此时，男主角吴争和兄弟史非都喜欢上了林小欧，出于兄弟情义，吴争一直回避着自己对林小欧的感情。</p>
                        <p>而林小欧，明确拒绝了史非的追求，主动对吴争坦露心迹。</p>
                        <p>一次失恋，并没有让林小欧对爱情灰心，遇到喜欢的男人，她也敢于主动追求。</p>
                        <p>林小欧和吴争的感情遭到了父母的反对，面对阻碍，她既没有任凭父母安排，也没有跟父母闹决裂，而是创造机会，让双方互相了解，增进信任。</p>
                        <p>后来，吴争因为私人原因和林小欧分手，叶昕又回来了，趁虚而入，想让小欧回到他身边。</p>
                        <p>面对嘘寒问暖的叶昕，林小欧并没有因为一时的心软，而接受对方。</p>
                        <p>不管叶昕如何费尽心思取悦自己、如何讨好自己的爸妈，林小欧都很坚定，叶昕背叛过自己，<b>感情已经是过去式，两人再无可能</b>。</p>
                        <p>而自己的心里还有吴争，应该为这段感情，再做一次努力。</p>
                        <p><b>分手了就应该彻底分开，不留恋、不后悔。</b></p>
                        <p>喜欢的人还单身时，就勇敢去爱，不要让自己遗憾。</p>
                        <p>在一起时就应该好好珍惜，生活不易，彼此多一些体谅和包容。</p>
                        <p>每个人从成长到成熟都有一段过程。</p>
                        <p>最初的不成熟的、笨拙而鲁莽的爱情，常常无疾而终，却恰恰最让人刻骨铭心的。</p>
                        <p><b>听不懂《后来》的人是幸运的，因为他从来就没有遗憾，所以不懂其中的感伤。</b></p>
